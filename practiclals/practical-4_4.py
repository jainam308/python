mark=float(input("Enter your Marks :-  "))
if(mark>=90):
    print("A Grade")
elif(mark<=89 and mark>=80):
    print("B Grade")
elif(mark<=79 and mark>=70):
    print("C Grade")
elif(mark<=69 and mark>=60):
    print("D Grade")
elif(mark<=59 and mark>=50):
    print("E Grade")
elif(mark<50):
    print("Fail") 